/*
* @brief Сравнивает не более первых n байтов str1 и str2.

* @Options:
 * @param str1, str2 – указатели на сравниваемые строки.
 * @param n – количество символов для сравнения.

* @Return:
0 – если первые n символов сравниваемых строк идентичны.
Положительное число – если в первых n символах сравниваемых строк есть отличия и код первого 
отличающегося символа в строке str1 больше кода символа на той же позиции в строке str2.
Отрицательное число – в первых n символах сравниваемых строк есть отличия и код первого 
отличающегося символа в строке str1 меньше кода символа на той же позиции в строке str2.

* @Description:
Функция побайтно сравнивает коды символов двух строк, на которые указывают аргументы функции.
Сравнение прекращается если встретились отличающиеся символы. При этом возвращается отрицательное 
или положительное число (см. возвращаемые значения).
Если были проверены n символов или обе сравниваемые строки закончились, и отличий не было, то 
сравнение прекращается и возвращается ноль.
*/

