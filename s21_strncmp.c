int s21_strncmp(const char *str1, const char *str2, s21_size_t n_byte) {
    int different = 0;

    if (n_byte > 0) {
        for (s21_size_t i = 0; i < n_byte; i++) {
            if (str1[i] == '\0' || str1[i] != str2[i]) {
                different = str1[i] - str2[i];
                break;
            }
        }
    }
    return different;
}
